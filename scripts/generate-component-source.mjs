import fs from "node:fs";
import path from "node:path";

const projectRoot = process.cwd();
const outputFile = path.join(projectRoot, "lib", "component-source.ts");
const filesToScan = [
  path.join(projectRoot, "app"),
  path.join(projectRoot, "lib"),
];
const componentSearchDirs = [
  path.join(projectRoot, "components", "ui"),
  path.join(projectRoot, "components", "ai"),
  path.join(projectRoot, "components", "blocks"),
  path.join(projectRoot, "components", "examples"),
];
const componentNamePatterns = [
  /componentName="([^"]+)"/g,
  /componentName\s*:\s*"([^"]+)"/g,
];

const componentAliases = {
  CollapsibleSidebar: path.join(
    projectRoot,
    "components",
    "blocks",
    "Sidebar.tsx",
  ),
};

// Return every .ts and .tsx file inside a directory recursively.
function getTypeScriptFiles(directory) {
  const entries = fs.readdirSync(directory, { withFileTypes: true });
  const result = [];

  for (const entry of entries) {
    const fullPath = path.join(directory, entry.name);

    if (entry.isDirectory()) {
      result.push(...getTypeScriptFiles(fullPath));
      continue;
    }

    if (entry.isFile() && /\.(ts|tsx)$/.test(entry.name)) {
      result.push(fullPath);
    }
  }

  return result;
}

// Extract every componentName value used by the docs pages and docs data files.
function findReferencedComponentNames() {
  const names = new Set();

  for (const directory of filesToScan) {
    for (const filePath of getTypeScriptFiles(directory)) {
      const content = fs.readFileSync(filePath, "utf8");

      for (const pattern of componentNamePatterns) {
        const matches = content.matchAll(pattern);

        for (const match of matches) {
          names.add(match[1]);
        }
      }
    }
  }

  return [...names].sort();
}

// Map a component name to the source file that should appear in the code preview.
function findComponentSourceFile(componentName) {
  const aliasPath = componentAliases[componentName];

  if (aliasPath) {
    return aliasPath;
  }

  for (const directory of componentSearchDirs) {
    const candidate = path.join(directory, `${componentName}.tsx`);

    if (fs.existsSync(candidate)) {
      return candidate;
    }
  }

  return null;
}

// Build the final [componentName, sourceCode] pairs and fail if something is missing.
function buildSourceEntries() {
  const entries = [];
  const unresolved = [];

  for (const componentName of findReferencedComponentNames()) {
    const componentPath = findComponentSourceFile(componentName);

    if (!componentPath) {
      unresolved.push(componentName);
      continue;
    }

    const sourceCode = fs.readFileSync(componentPath, "utf8");
    entries.push([componentName, sourceCode]);
  }

  if (unresolved.length > 0) {
    throw new Error(
      `Could not resolve source files for: ${unresolved.join(", ")}`,
    );
  }

  return entries;
}

// Convert the collected entries into the generated TypeScript registry file.
function createGeneratedFile(entries) {
  const lines = [
    "// This file is generated by scripts/generate-component-source.mjs.",
    "// Do not edit it manually.",
    "",
    "export const componentSourceByName: Record<string, string> = {",
  ];

  for (const [componentName, sourceCode] of entries) {
    lines.push(`  ${JSON.stringify(componentName)}: ${JSON.stringify(sourceCode)},`);
  }

  lines.push("};", "", "export function getComponentSource(componentName: string) {");
  lines.push("  return componentSourceByName[componentName];");
  lines.push("}");

  return `${lines.join("\n")}\n`;
}

// Run the generator and write the output used by ComponentShowcase at runtime.
const entries = buildSourceEntries();
const generatedFile = createGeneratedFile(entries);

fs.writeFileSync(outputFile, generatedFile);
console.log(
  `Generated ${path.relative(projectRoot, outputFile)} with ${entries.length} entries.`,
);
